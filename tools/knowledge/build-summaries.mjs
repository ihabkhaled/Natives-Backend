#!/usr/bin/env node
import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const REAL_REPO_ROOT = path.resolve(
  path.dirname(fileURLToPath(import.meta.url)),
  '..',
  '..',
);
const APPROX_CHARS_PER_TOKEN = 4;
const TOKEN_BUDGET = 1500;

// Extracts the body of a `## Heading` section (everything up to the next
// `## ` line or end of file) from a markdown document. Plain string search,
// not a regex lookahead race between "next heading" and "end of string" —
// `$` inside a lookahead under the `m` flag matches at *every* line end
// (including right after the heading itself), which silently produces an
// empty capture. Boring string slicing has no such footgun.
export function extractSection(docText, headingText) {
  const headingLine = `## ${headingText}`;
  const headingIndex = docText.indexOf(`${headingLine}\n`);
  if (headingIndex === -1) {
    return null;
  }
  const bodyStart = headingIndex + headingLine.length;
  const nextHeadingIndex = docText.indexOf('\n## ', bodyStart);
  const bodyEnd = nextHeadingIndex === -1 ? docText.length : nextHeadingIndex;
  return docText.slice(bodyStart, bodyEnd).trim();
}

// Rule-category headers from rules/00, minus the trailing checklist section
// — parsed live so BOOTSTRAP.md never drifts from the real rule count as
// rules/00 grows (it already has, more than once, during this delivery).
export function extractRuleCategories(nonNegotiableRulesText) {
  const categories = [];
  for (const match of nonNegotiableRulesText.matchAll(/^##\s+(.+)$/gm)) {
    const heading = match[1].trim();
    if (heading.toLowerCase().includes('checklist')) {
      continue;
    }
    categories.push(heading);
  }
  return categories;
}

export function approxTokens(text) {
  return Math.ceil(text.length / APPROX_CHARS_PER_TOKEN);
}

function readIfExists(repoRoot, relativePath) {
  const filePath = path.join(repoRoot, relativePath);
  return existsSync(filePath) ? readFileSync(filePath, 'utf8') : null;
}

const GATE_SCRIPT_NAMES = ['lint', 'typecheck', 'test:coverage', 'build'];

function extractGateCommands(packageJsonText) {
  const packageJson = JSON.parse(packageJsonText);
  return GATE_SCRIPT_NAMES.filter(name => name in packageJson.scripts).map(
    name => `npm run ${name}`,
  );
}

// Builds BOOTSTRAP.md's content from claude.md, rules/00, and package.json —
// pure string-in/string-out so it is testable without touching disk.
export function buildBootstrapContent({
  claudeMdText,
  nonNegotiableRulesText,
  packageJsonText,
}) {
  const packageJson = JSON.parse(packageJsonText);
  const purpose =
    extractSection(claudeMdText, 'Purpose') ??
    packageJson.description ??
    'See claude.md.';
  const ruleCategories = extractRuleCategories(nonNegotiableRulesText);
  const gateCommands = extractGateCommands(packageJsonText);

  const lines = [
    '# Bootstrap',
    '',
    `> Generated by \`npm run knowledge:build\` from claude.md, rules/00-non-negotiable-rules.md, and package.json. Do not edit by hand — see tools/knowledge/build-summaries.mjs.`,
    '',
    '## What this is',
    '',
    purpose.split(/\n\n/)[0],
    '',
    '## Authority (read in this order when guidance conflicts)',
    '',
    '1. `claude.md` — canonical governance policy.',
    '2. `rules/00-non-negotiable-rules.md` + `context/architecture-map.md` — the engineering canon.',
    "3. The layer rule for what you're touching (`rules/README.md`).",
    '4. `AGENTS.md` / `.cursor/rules/*.mdc` / `.cursorrules` / `codex.md` / `cursor.md` / the 5 family entrypoints — compatibility mirrors, never richer than `claude.md`.',
    '',
    `## Non-negotiable rule categories (${ruleCategories.length ? 'see rules/00' : 'none found'})`,
    '',
    ...ruleCategories.map(category => `- ${category}`),
    '',
    '## Fast task protocol',
    '',
    '1. Run `npm run knowledge:context -- --task="<what you\'re about to do>"`.',
    '2. Read `.ai/local/current-context.md` — the warm-up set plus ranked rules/skills/source for this task.',
    '3. Read the exact source and tests it names before planning.',
    '4. Run the Simple Code Ladder (`rules/20-simple-readable-code.md`) before writing new code.',
    '',
    '## Quality gates (all green before done)',
    '',
    '```bash',
    ...gateCommands,
    '```',
    '',
    'Never bypass a hook with `--no-verify`. A red gate is fixed at the root cause, never suppressed.',
    '',
  ];

  return `${lines.join('\n')}\n`;
}

export function buildBootstrap(repoRoot = REAL_REPO_ROOT) {
  const claudeMdText = readIfExists(repoRoot, 'claude.md');
  const nonNegotiableRulesText = readIfExists(
    repoRoot,
    'rules/00-non-negotiable-rules.md',
  );
  const packageJsonText = readIfExists(repoRoot, 'package.json');

  if (
    claudeMdText === null ||
    nonNegotiableRulesText === null ||
    packageJsonText === null
  ) {
    throw new Error(
      'build-summaries.mjs requires claude.md, rules/00-non-negotiable-rules.md, and package.json to exist',
    );
  }

  const content = buildBootstrapContent({
    claudeMdText,
    nonNegotiableRulesText,
    packageJsonText,
  });

  mkdirSync(path.join(repoRoot, '.ai'), { recursive: true });
  writeFileSync(path.join(repoRoot, '.ai', 'BOOTSTRAP.md'), content, 'utf8');

  return { content, tokens: approxTokens(content) };
}

const isMain = process.argv[1] === fileURLToPath(import.meta.url);
if (isMain) {
  const { tokens } = buildBootstrap();
  if (tokens > TOKEN_BUDGET) {
    console.warn(
      `knowledge:build — .ai/BOOTSTRAP.md is ~${tokens} tokens, over its ${TOKEN_BUDGET}-token budget. ` +
        'Trim a section in tools/knowledge/build-summaries.mjs.',
    );
  } else {
    console.log(
      `knowledge:build — .ai/BOOTSTRAP.md written (~${tokens} tokens)`,
    );
  }
}
